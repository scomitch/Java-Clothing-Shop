    public ViewPreviousOrders(Customer customer) {
        //Init global variables with passed parameters.
        loggedCustomer = customer;
        
        initComponents();

        //Get the Table Model from the orders table in the frame.
        DefaultTableModel ordersModel = (DefaultTableModel)tblOrders.getModel();

        //Loop through the logged Customers orders.
        for(Map.Entry<Integer, Order> olEntry : loggedCustomer.getOrders().entrySet()){
            //Create order class with the entry.
            Order order = olEntry.getValue();

            //If an order is marked as complete.
            if(order.getOrderStatus().equals("Complete")){
                //Add it to the table view.
                ordersModel.addRow(new Object[] { order.getOrderId(), order.getOrderDate(), "Â£" + String.format("%.02f", order.getOrderTotal()) });
            }
        }
    }

