    private void btnOrderDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderDetailsActionPerformed
        //Get the selected row from the orders table.
        int selectedRow = tblOrders.getSelectedRow();

        //If no row is selected then return an error.
        if(selectedRow == -1){
            lblError.setText("Please select an order first");
        } else {
            //Grab the order ID from the first column of the orders table.
            int orderId = Integer.parseInt(String.valueOf(tblOrders.getValueAt(selectedRow, 0)));

            //Frame transfer to the View Previous Order Lines with the OrderId
            ViewPreviousOrderLines viewOrderLines = new ViewPreviousOrderLines(loggedCustomer, orderId);
            this.dispose();
            viewOrderLines.setVisible(true);
        }
    }//GEN-LAST:event_btnOrderDetailsActionPerformed

