    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        //Grabs the text contents of product.
        String name = txtName.getText();
        String price = txtPrice.getText();
        String stock = txtStock.getText();
        String extraAttribute = txtDynamicInfo.getText();

        //Validation checks to see if content is selected / missing.
        if(!radFootwear.isSelected() && !radClothing.isSelected())
        {
            lblError.setText("Error: Please Select a Category");
        }
        else if(name.equals("") || price.equals("") || stock.equals("") || extraAttribute.equals(""))
        {
            lblError.setText("Error: Fill out all Fields");
        }
        else{
            try {
                //Changes the string forms to Integer.
                double intPrice = Double.parseDouble(price);
                int intStock = Integer.parseInt(stock);
                
                //If clothing is selected.
                if(radClothing.isSelected()){
                    if(!txtDynamicInfo.equals("")){
                        //Create Clothing class with product information and add it to db.
                        Clothing clothing = new Clothing(name, intPrice, intStock, extraAttribute);
                        DBUtil db = new DBUtil();
                        db.addProduct(clothing);

                        //Clear the form.
                        clearForm();

                        lblError.setText("Added new clothing");
                    } else {
                        lblError.setText("Enter a measurement or size for the product.");
                    }
                    
                } else {

                    //Check to see if the number is numeric.
                    boolean number = isNumeric(extraAttribute);

                    //Can't be <= 0 for the size.
                    if(number && Integer.parseInt(extraAttribute) > 1){
                        //Create footwear class and add it to database.
                        Footwear footwear = new Footwear(name, intPrice, intStock, Integer.parseInt(extraAttribute));
                        DBUtil db = new DBUtil();
                        System.out.println(name + " @Â£" + intPrice + " with stock " + intStock + " and size " + Integer.parseInt(extraAttribute));
                        db.addProduct(footwear);

                        //Clear the form
                        clearForm();
                        
                        lblError.setText("Added new footwear.");
                    } else {
                        lblError.setText("Footwear size must be a number greater than 1");
                    }
                    
                }
                
            } catch (Exception ex) {
                lblError.setText("Issue adding product");
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

