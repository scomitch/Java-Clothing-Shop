    //Method to check if a string variation is numeric.
    public static boolean isNumeric(String num){
        try {
            //Attempt to parse it to double.
            double d = Double.parseDouble(num);
        } catch (NumberFormatException | NullPointerException nfe) {
            //Isn't numeric if exception is called.
            return false;
        }
        //Else it is numeric.
        return true;
    }

