    private void clearForm(){
        //Check if it's clothing or footwear and revert changes.
        if(product.getClass().getName().equals("Models.Clothing")){
            radClothing.setSelected(true);
            lblDynamic.setText("Measurement");
            
            Clothing clothing = (Clothing)product;
            txtDynamicInfo.setText(String.valueOf(clothing.getMeasurement()));        
        } else {
            radFootwear.setSelected(true);
            lblDynamic.setText("Size");
            
            Footwear footwear = (Footwear)product;
            txtDynamicInfo.setText(String.valueOf(footwear.getSize()));
        }
        
        radClothing.setEnabled(false);
        radFootwear.setEnabled(false);
        
        txtName.setText(product.getProductName());
        txtPrice.setText(String.valueOf(product.getPrice()));
        txtStock.setText(String.valueOf(product.getStockLevel()));
    }
    
