    public ViewPreviousOrderLines(Customer customer, int oID) {
        //Init global vars with passed parameters.
        loggedCustomer = customer;
        orderId = oID;
        
        initComponents();

        //Get the orderLinesModel from the frame.
        DefaultTableModel orderLinesModel = (DefaultTableModel)tblOrderLines.getModel();

        //Create an order class with the orderID from the logged customer.
        Order orderToView = loggedCustomer.getOrders().get(orderId);
        
        lblOrderDetails.setText("Your Order ID " + orderId + " in Detail");

        //Loop through the orderLines.
        for(Map.Entry<Integer, OrderLine> olEntry : orderToView.getOrderLines().entrySet()){
            OrderLine ol = olEntry.getValue();
            //Get the orderLine and add the row to the table view.
            orderLinesModel.addRow(new Object[] {ol.getProduct().getProductID(), ol.getProduct().getProductName(), "Â£" + String.format("%.02f", ol.getLineTotal())});
        }
    }

