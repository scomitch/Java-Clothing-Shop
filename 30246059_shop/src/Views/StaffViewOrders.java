    private void btnOrderDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderDetailsActionPerformed
        //Get the row ID from the order table.
        int selectedRow = tblOrders.getSelectedRow();

        //If the table doesn't return a row throw error.
        if(selectedRow == -1){
            lblError.setText("Please select an order first");
        } else {
            //Grab the customer username from the Orders table.
            String cusUsername = String.valueOf(tblOrders.getValueAt(selectedRow, 0));
            //Grab the Order ID from the orders table.
            int orderID = Integer.parseInt(String.valueOf(tblOrders.getValueAt(selectedRow, 1)));

            //Frame transfer to the Detailed Order sheet with the Order ID to show.
            StaffViewOrderLines staffLines = new StaffViewOrderLines(loggedStaff, cusUsername, orderID);
            this.dispose();
            staffLines.setVisible(true);
        }
    }//GEN-LAST:event_btnOrderDetailsActionPerformed

