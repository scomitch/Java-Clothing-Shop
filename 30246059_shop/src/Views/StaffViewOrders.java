    public StaffViewOrders(Staff staff) {
        //Init variables with passed parameters.
        loggedStaff = staff;
        
        initComponents();

        //Load the database and customers.
        DBUtil db = new DBUtil();
        customers = db.loadCustomers();

        //Get the list model from the orders.
        DefaultTableModel ordersModel = (DefaultTableModel)tblOrders.getModel();

        //Lop through the current customers/
        for(Map.Entry<String, Customer> cusEntry : customers.entrySet()){
            //Create customer class with the entry value.
            Customer cus = cusEntry.getValue();
            //Loop through orders relating to that customer.
            for(Map.Entry<Integer, Order> oEntry : cus.getOrders().entrySet()){
                //Create order class from the entry.
                Order order = oEntry.getValue();

                //If the order is marked as Complete.
                if(order.getOrderStatus().equals("Complete")){
                    //Add the row to the TableModel.
                    ordersModel.addRow(new Object[] { cus.getUsername(), order.getOrderId(), order.getOrderDate(), "Â£" + String.format("%.02f", order.getOrderTotal()) });
                }
            }
            
        }
    }

