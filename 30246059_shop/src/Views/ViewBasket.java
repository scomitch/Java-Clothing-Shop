    private void btnRemoveProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveProductActionPerformed
        //Get the selected row from the basket table.
        int selectedRow = tblBasket.getSelectedRow();

        //If nothing is selected send an error.
        if(selectedRow == -1){
            lblError.setText("Please highlight a product to remove from your basket");
        } else {
            //Get product ID from the value of the row selected.
            int productID = Integer.parseInt(String.valueOf(tblBasket.getValueAt(selectedRow, 0)));
            System.out.println("Product ID To remove " + productID);

            //Remove the orderLine with the productID from the currentOrder.
            currentOrder.removeOrderLine(productID);
            
            lblError.setText("Product Removed");

            //Get the TableModel from the current basket table.
            DefaultTableModel basketModel = (DefaultTableModel)tblBasket.getModel();
            //Remove the row from the table.
            basketModel.removeRow(selectedRow);
            //Set the basket model to the new model.
            tblBasket.setModel(basketModel);

            //Update the order total.
            lblOrderTotal.setText("£" + String.format("%.02f", currentOrder.getOrderTotal()));
        }
    }//GEN-LAST:event_btnRemoveProductActionPerformed

    private void btnBuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuyActionPerformed
        //If the current order has no order lines then there are no products to buy.
        if(currentOrder.getOrderLines().isEmpty()){
            lblError.setText("No products to buy!");
        } else {
            //Set a canBuy boolean for validation purposes later.
            boolean canBuy = true;
            //Loop through the current order lines.
            for(Map.Entry<Integer, OrderLine> olEntry: currentOrder.getOrderLines().entrySet()){
                OrderLine ol = olEntry.getValue();

                //If the current stock level for that product is 0 then it is out of stock.
                if(ol.getProduct().getStockLevel() == 0){
                    //canBuy validation bool set to false.
                    canBuy = false;
                    lblError.setText("Product " + ol.getProduct().getProductName() + " is out of stock.");
                }
            }

            //If the canBuy validation passes
            if(canBuy){
                //Complete the order.
                currentOrder.completeOrder();

                //Frame transfer to the Order Confirmation page.
                OrderConfirmation confirm = new OrderConfirmation(loggedCustomer, currentOrder);
                this.dispose();
                confirm.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnBuyActionPerformed

