    public ViewBasket(Customer customer) {
        //Init global vars with passed parameter.
        loggedCustomer = customer;
        currentOrder = customer.findLatestOrder();
        
        
        initComponents();

        //Get the table model from the form.
        DefaultTableModel basketModel = (DefaultTableModel)tblBasket.getModel();

        //Loop through the currentOrder of the customer.
        for(Map.Entry<Integer, OrderLine> olEntry : currentOrder.getOrderLines().entrySet()){
            //Create a product class from the current entry.
            OrderLine orderLine = olEntry.getValue();
            Product product = orderLine.getProduct();

            //Add the orderLine to the table model.
            basketModel.addRow(new Object[] {product.getProductID(), product, "£" + String.format("%.02f", product.getPrice()) , orderLine.getQuantity()});
            
        }

        //Set the order total text.
        lblOrderTotal.setText("£" + String.format("%.02f", currentOrder.getOrderTotal()));
    }

